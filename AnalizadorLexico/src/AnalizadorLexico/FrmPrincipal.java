   private boolean analizarLexico() throws IOException { //analisis Léxico usando el primer lexer que creamos
        int cont = 1; //Número de línea
        String expr = (String) txtResultado.getText(); //Obtengo el texto en pantalla
        Lexer lexer = new Lexer(new StringReader(expr)); //Crea instancia del analizador léxico Lexer.java y le pasa el código fuente convertido en un flujo de lectura
        //Este lexer devuelve los Enum que yo cree, es un poco más estético que el otro por eso hay 2 pero tienen los mismos tokens
        String resultado = "LINEA " + cont + "\t\tSimbolo\n\n"; //Resultado es lo que se muestra en pantalla
        boolean hayErrores = false; //Funciona de bandera para saber si se encontró un símbolo inválido durante el proceso
        
        try {
            while (true) { //lee token por token hasta que termine el archivo
                Tokens token = lexer.yylex();
                if (token == null) { //Cuando se acabe el archivo
                    txtAnalizarLex.setText(resultado); //muestra en pantalla
                    return !hayErrores; //retorna true si no hubo errores 
                }
                switch (token) { //Va completando el resultado
                    case Linea:
                        cont++;
                        resultado += "LINEA " + cont + "\n\n";
                        break;
                    case ERROR:
                        resultado += "\t\t ERROR: Simbolo no definido\n";
                        hayErrores = true;
                        break;
                    default:
                        resultado += "\t\t" + lexer.lexeme + " -> Token: " + token + "\n";
                        break;
                }
            }
        } catch (IOException ex) {
            txtAnalizarLex.setText("Error grave al analizar el léxico: " + ex.getMessage()); //si hay algún error lo muestra acá
            return false;
        }
    }
