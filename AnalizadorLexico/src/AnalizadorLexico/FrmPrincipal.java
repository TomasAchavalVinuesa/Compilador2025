    private boolean analizarSintactico() {
        String ST = txtResultado.getText(); //obtiene el contenido del "IDE"
        ST = ST.trim(); //quita los espacios del inicio y final para evitar errores "EOF"

        Sintax s = new Sintax(new AnalizadorLexico.LexerCup(new StringReader(ST))); //instancia un Lexer que le devuelve los símbolos para usar en el análisis sintáctico 
        //Este lexer es otro al que usamos  en el analisisLexico 
        try {
            java_cup.runtime.Symbol rootSymbol = s.parse(); //Acá el parser pide todos los tokens uno a uno y valida la gramatica y va ejecutando las acciones para crear el AST
            //rootSymbol es el resultado final de eso. y en su atributo value va lo que devuelve la regla INICIO del Sintax.cup (o sea el nodo programa)

            if (rootSymbol.value instanceof NodoPrograma) { //Acá comprueba que sea una instancia válida de AST
                this.raizAST = (NodoPrograma) rootSymbol.value; //Guarda el árbol generado en una variable global de la clase para usarla en otros lados

                txtAnalizarSin.setText("Análisis Sintáctico realizado correctamente."); //avisa que salió todo bien
                txtAnalizarSin.setForeground(new Color(25, 111, 61)); 

                
                mostrarArbolAST(this.raizAST);  //llama al método para mostrar el AST

                return true; //avisa que salió todo bien al método de compilación
            } else {
                txtAnalizarSin.setText("Error: No se pudo construir el árbol sintáctico."); //si el AST creado no es válido, muestra el error y devuelve false terminando el proceso de compilación
                txtAnalizarSin.setForeground(Color.red);
                return false;
            }

        } catch (Exception ex) { //si el código está mal escrito entramos acá
            Symbol sym = s.getS(); //recupera el simbolo del problema
            txtAnalizarSin.setForeground(Color.red);
            if (sym != null) { //si sabemos donde es el problema lo avisamos de forma específica
                txtAnalizarSin.setText("Error de Sintaxis. Línea: " + (sym.right + 1) + " Caracter: " + (sym.left + 1) + ", Texto: \"" + sym.value + "\"");
            } else {//si desconocemos el problema damos un mensaje general 
                txtAnalizarSin.setText("Error de Sintaxis catastrófico (posiblemente caracteres al final del archivo)");
            }
            return false;
        }
    }
    
    private void mostrarTablaSimbolos(TablaSimbolos ts) {
        if (ts == null) { //si no hay tabla
            return;
        }
        String tablaString = ts.imprimir(); //llamamos al método imprimir de la tabla y luego la mostramos en pantalla
        txtTablaSimbolos.setText(tablaString); 
    }
    
    private boolean analizarSemantico() {
        if (this.raizAST == null) { //necesita un AST para hacer el analisis
            txtAnalizarSem.setText("No se puede realizar análisis semántico sin un AST válido.");
            return false;
        }

        TablaSimbolos ts = new TablaSimbolos(); //instancia una tabla de simbolos

        try {
            //Ejecutar el análisis semántico mediante el AST en forma de cascada
            this.raizAST.analizar(ts);

            //Si no hubo error en lo anterior decimos que es un éxito
            txtAnalizarSem.setText("Análisis Semántico realizado correctamente.");
            txtAnalizarSem.setForeground(new Color(25, 111, 61)); // Verde

            mostrarTablaSimbolos(ts); //llamamos a la función para mostrar la tabla de símbolos

            return true;

        } catch (ExcepcionSemantica ex) {
            txtAnalizarSem.setText("Error Semántico: " + ex.getMessage()); //si hubo una excepción la avisamos por pantalla
            txtAnalizarSem.setForeground(Color.red);
            return false;
        }
    }
    
    private void generar3DC() {
        if (this.raizAST == null){ //si el AST no existe, no hay nada que hacer acá
            return;
        }
        String codigo = (String) this.raizAST.generarCodigo(); //esto va a iniciar la cascada de generación de código 3DC
        txtCodigo3DC.setText(codigo); //mostramos lo que se generó en pantalla
    }
    
    private void generarAssembly() {
        //Traemos el código 3DC generado previamente
        String codigo3DC = txtCodigo3DC.getText();
        
        if (codigo3DC == null || codigo3DC.trim().isEmpty()) {
            txtCodigoAssembly.setText("No hay código 3DC para traducir.");
            return;
        }

        //Instanciamos su traductor
        GeneradorAssembly genAsm = new GeneradorAssembly();
        String codigoAsm = genAsm.generar(codigo3DC); //le pedimos que lo traduzca
        
        txtCodigoAssembly.setText(codigoAsm); //mostramos la traducción en pantalla
    }
    
    private void compilar() throws IOException {
        limpiarPantallas(); //Borra los resultados visibles de la anterior compilación
    
        if (!analizarLexico()) { //llama al método para el análisis léxico en caso de error  se muestra esto en pantalla y se finaliza el proceso de compilación
            txtAnalizarLex.append("\n Se ha encontrado un error en este proceso, se ha detenido el proceso de compilación");
            return;
        }
        
        if (!analizarSintactico()) { //llama al método para el análisis sintactico en caso de error  se muestra esto en pantalla (también en el Ast) y se finaliza el proceso de compilación
            txtAnalizarSin.append("\n Se ha encontrado un error en este proceso, se ha detenido el proceso de compilación");
            txtAST.append("\n Se ha encontrado un error en este proceso, se ha detenido el proceso de compilación");
            return; // Detenerse si la sintaxis falla (no hay AST)
        }
        
        if (!analizarSemantico()) {//llama al método para el análisis Semántico en caso de error  se muestra esto en pantalla y se finaliza el proceso de compilación
            txtAnalizarSem.append("\n Se ha encontrado un error en este proceso, se ha detenido el proceso de compilación");
            txtTablaSimbolos.append("\n Se ha encontrado un error en este proceso, se ha detenido el proceso de compilación");
            return; // Detenerse si la sintaxis falla (no hay AST)
        }
        generar3DC(); //si pasaron los análisis entonces se debe poder generar el código 3DC
        
        if (!txtCodigo3DC.getText().isEmpty() && !txtCodigo3DC.getText().startsWith("Error")) { //si el código 3DC está vacio o empieza con error este paso se saltea
            generarAssembly();
            System.out.println("Compilación realizada exitosamente");
        }
}
    
    private void limpiarPantallas() { //Todos los cuadros de texto no editables se vacían con este método (solo no afecta al que actua de IDE)
        txtAnalizarLex.setText(null);
        txtAnalizarSin.setText(null);
        txtAnalizarSem.setText(null);
        txtAST.setText(null);
        txtTablaSimbolos.setText(null);
        txtCodigo3DC.setText(null);
        txtCodigoAssembly.setText(null);
    }
    
